    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        List<FAQ> listFAQ = FAQ.getAllFAQ();
        
        for (int i = 0; i < listFAQ.size(); i++) {
            FAQ tempFAQ = listFAQ.get(i);
            int margin = 20;
            JLabel title = new JLabel(tempFAQ.question);
            Font titleFont = new Font("Open Sans",Font.BOLD,22);
            int y1;
            try {
                y1 = contentPanel.getComponent(contentPanel.getComponentCount()-1).getY()+contentPanel.getComponent(contentPanel.getComponentCount()-1).getHeight();
            } catch (Exception ex) {
                y1 = 0;
            }
            
            title.setBounds(0, y1, this.getWidth(), title.getPreferredSize().height+margin);
            title.setFont(titleFont);
            contentPanel.add(title);
            JTextArea desc = new JTextArea(tempFAQ.answer);
            desc.setWrapStyleWord(true);
            desc.setLineWrap(true);
            desc.setOpaque(false);
            desc.setEditable(false);
            desc.setFocusable(false);
            desc.setBackground(UIManager.getColor("Label.background"));
            desc.setFont(UIManager.getFont("Label.font"));
            desc.setBorder(UIManager.getBorder("Label.border"));
            Font descFont = new Font("Open Sans",Font.PLAIN,16);
            desc.setFont(descFont);
            int y2 = contentPanel.getComponent(contentPanel.getComponentCount()-1).getY()+contentPanel.getComponent(contentPanel.getComponentCount()-1).getHeight();
            desc.setBounds(0, y2, this.getWidth(), desc.getPreferredSize().height+margin);
            contentPanel.add(desc);
            System.out.println(i);
        }
        int y2 = contentPanel.getComponent(contentPanel.getComponentCount()-1).getY()+contentPanel.getComponent(contentPanel.getComponentCount()-1).getHeight();
        contentPanel.setPreferredSize(new Dimension(contentPanel.getWidth(),y2));
    }//GEN-LAST:event_formComponentShown

