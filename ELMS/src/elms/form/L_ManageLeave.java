    public static void refreshTable(final DefaultTableModel model) {
        for (int i = model.getRowCount() - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        List<Leave> FAQ1 = Leave.getUserLeave(User.getInstance().ID);
        for (int i = 0; i < FAQ1.size(); i++) {
            Leave faq = FAQ1.get(i);
            Object[] row = {i + 1, faq.getID(), faq.getTitle(), faq.getType(), faq.getStartDate(), faq.getDuration(), faq.getStatus()};
            model.addRow(row);
        }
    }

