    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int confirm = JOptionPane.showConfirmDialog(this, "Do you really want to delete this leave?", "Confirm deleting leave", 0);
            if (confirm == JOptionPane.YES_OPTION) {
                int a = jTable1.getSelectedRow();
                Leave.deleteLeave((int) jTable1.getValueAt(a, 1));
                if (((String) jTable1.getValueAt(a, 6)).equals("Pending")) {
                    User.addLeave(User.getInstance().ID, (int) jTable1.getValueAt(a, 1));
                }
                refreshTable((DefaultTableModel) jTable1.getModel());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Select A Leave.", "No leave selected", JOptionPane.ERROR_MESSAGE);
        }
        this.dispose();
        new L_ManageLeave().setVisible(true);
    }//GEN-LAST:event_jButton7ActionPerformed

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        refreshTable((DefaultTableModel) jTable1.getModel());
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int a = jTable1.getSelectedRow();
                    if (a != -1) {
                        if (((String) jTable1.getValueAt(a, 6)).equals("Pending")) {
                            jButton12.setEnabled(true);
                        } else {
                            jButton12.setEnabled(false);
                        }
                    }
                }
            }
        });
    }//GEN-LAST:event_formComponentShown

